import math

fatorial_30 = math.factorial(30)
maiorvalorinteiroCpp = 2147483647 # maior valor inteiro em C/C++

print(f"Fatorial de 30 em Python: {fatorial_30}")

if fatorial_30 > maiorvalorinteiroCpp:
    print("O fatorial de 30 é maior que o maior valor inteiro em C/C++.")
else:
    print("O fatorial de 30 é menor ou igual ao maior valor inteiro em C/C++.")
    



